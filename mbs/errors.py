###############################################################################
# MBSException
###############################################################################
class MBSException(Exception):

    ###########################################################################
    def __init__(self, message, cause=None):
        Exception.__init__(self, message)
        self._cause = cause

    ###########################################################################
    @property
    def cause(self):
        return self._cause

    ###########################################################################
    def __str__(self):
        return self.message

###############################################################################
# ConfigurationError
###############################################################################
class ConfigurationError(MBSException):

    ###########################################################################
    def __init__(self, message, cause=None):
        MBSException.__init__(self, message, cause=cause)